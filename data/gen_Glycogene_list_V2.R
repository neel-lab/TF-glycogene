library(dplyr)
library(reshape2)
library(tidyr)
load('Glycogene_list.rda')
load('pathLists_V2.rda')
load('./processed/EP_MasterMatrix.rda')
load('./processed/RP_MasterMatrix.rda')
#Merge glycogene pathways with Glycogene list:
Glycogenes=unique(c(unlist(pathLists),Glycogenes))
length(Glycogenes)
Glycogenes=unique(Glycogenes)
length(Glycogenes)
merge_data_glycogenes=RP_MasterMatrix %>% filter(gene %in% Glycogenes) %>% left_join(EP_MasterMatrix,by=c('TF','gene','ctype')) %>% distinct()
totalRelations=merge_data_glycogenes %>% nrow()
threshRelations=merge_data_glycogenes %>% filter(RP>=0.5,cor>=0.4) %>% nrow()
message(paste(paste('Number of TF-GG relationshipss across all cancers:',totalRelations,sep=' '),
	      paste('Number of TF-GG relationships with RP>=0.5 & cor>=0.4:',threshRelations,sep=' ')
	     ,sep='\n')
)
save(file='Glycogene_list_V2.rda',Glycogenes)
save(file='./processed/merge_data_glycogenes_V2.rda',merge_data_glycogenes)
